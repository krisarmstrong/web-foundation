export function createSocialLinks(username: string, options?: {
  youtube?: string;
  facebook?: string;
  instagram?: string;
}): SocialIcon[] {
  const links: SocialIcon[] = [
    {
      label: 'GitHub',
      href: `https://github.com/${username}`,
      icon: SOCIAL_ICONS.github(),
    },
    {
      label: 'LinkedIn',
      href: `https://www.linkedin.com/in/${username}/`,
      icon: SOCIAL_ICONS.linkedin(),
    },
    {
      label: 'X',
      href: `https://x.com/${username}`,
      icon: SOCIAL_ICONS.x(),
    },
  ];

  if (options?.youtube) {
    links.push({
      label: 'YouTube',
      href: `https://youtube.com/@${options.youtube}`,
      icon: SOCIAL_ICONS.youtube(),
    });
  }

  if (options?.facebook) {
    links.push({
      label: 'Facebook',
      href: `https://facebook.com/${options.facebook}`,
      icon: SOCIAL_ICONS.facebook(),
    });
  }

  if (options?.instagram) {
    links.push({
      label: 'Instagram',
      href: `https://instagram.com/${options.instagram}`,
      icon: SOCIAL_ICONS.instagram(),
    });
  }

  return links;
}
